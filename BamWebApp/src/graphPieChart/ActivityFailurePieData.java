package graphPieChart;

public class ActivityFailurePieData {

	
	private String failureActivity;
	
	private int noFailureActivities;
	
	
	

	public String getFailureActivity() {
		return failureActivity;
	}
	public void setFailureActivity(String failureActivity) {
		this.failureActivity = failureActivity;
	}
	
	public int getNoFailureActivities() {
		return noFailureActivities;
	}
	public void setNoFailureActivities(int noFailureActivities) {
		this.noFailureActivities = noFailureActivities;
	}
	
	
	

}
